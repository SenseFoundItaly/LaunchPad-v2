/** Generate print-friendly HTML from skill output content */
export function generatePrintHTML(title: string, content: string): string {
  // Strip artifact blocks
  const clean = content.replace(/:::artifact[\s\S]*?:::/g, '');

  // Convert markdown to HTML
  const lines = clean.split('\n');
  let html = '';
  let inList = false;

  for (const line of lines) {
    const trimmed = line.trim();
    if (!trimmed) {
      if (inList) { html += '</ul>'; inList = false; }
      continue;
    }

    // Headings
    if (trimmed.startsWith('# ')) { html += `<h1>${esc(trimmed.slice(2))}</h1>`; continue; }
    if (trimmed.startsWith('## ')) { html += `<h2>${esc(trimmed.slice(3))}</h2>`; continue; }
    if (trimmed.startsWith('### ')) { html += `<h3>${esc(trimmed.slice(4))}</h3>`; continue; }

    // Section dividers
    if (trimmed.match(/^[━═─]{3,}/) || trimmed.startsWith('---')) {
      html += '<hr/>';
      continue;
    }
    if (trimmed.match(/^[━═─]+\s*.+\s*[━═─]+$/)) {
      const sectionTitle = trimmed.replace(/[━═─]/g, '').trim();
      html += `<h2 class="section-header">${esc(sectionTitle)}</h2>`;
      continue;
    }

    // Bullets
    if (trimmed.startsWith('- ') || trimmed.startsWith('* ') || trimmed.startsWith('\u2192 ')) {
      if (!inList) { html += '<ul>'; inList = true; }
      const text = trimmed.replace(/^[-*\u2192]\s*/, '');
      html += `<li>${bold(esc(text))}</li>`;
      continue;
    }

    if (inList) { html += '</ul>'; inList = false; }
    html += `<p>${bold(esc(trimmed))}</p>`;
  }
  if (inList) html += '</ul>';

  const date = new Date().toLocaleDateString('en-US', { year: 'numeric', month: 'long', day: 'numeric' });

  return `<!DOCTYPE html>
<html>
<head>
<meta charset="utf-8"/>
<title>${esc(title)} — LaunchPad</title>
<style>
  @page { margin: 2cm; }
  body { font-family: Georgia, 'Times New Roman', serif; color: #1a1a1a; line-height: 1.6; max-width: 700px; margin: 0 auto; padding: 40px 20px; }
  .header { border-bottom: 2px solid #333; padding-bottom: 16px; margin-bottom: 24px; }
  .header h1 { font-family: system-ui, sans-serif; font-size: 24px; margin: 0 0 4px 0; }
  .header .meta { font-size: 12px; color: #666; }
  h1 { font-family: system-ui, sans-serif; font-size: 20px; margin: 28px 0 8px 0; }
  h2 { font-family: system-ui, sans-serif; font-size: 16px; margin: 24px 0 6px 0; }
  h2.section-header { background: #f0f0f0; padding: 8px 12px; border-radius: 4px; font-size: 13px; text-transform: uppercase; letter-spacing: 1px; margin: 28px 0 12px 0; }
  h3 { font-family: system-ui, sans-serif; font-size: 14px; margin: 16px 0 4px 0; }
  p { margin: 6px 0; font-size: 14px; }
  ul { margin: 8px 0; padding-left: 24px; }
  li { margin: 4px 0; font-size: 14px; }
  hr { border: none; border-top: 1px solid #ddd; margin: 20px 0; }
  strong { font-weight: 700; }
  @media print { body { padding: 0; } .header { page-break-after: avoid; } h2.section-header { page-break-before: auto; } }
</style>
</head>
<body>
<div class="header">
  <h1>${esc(title)}</h1>
  <div class="meta">Generated by LaunchPad | ${date}</div>
</div>
${html}
</body>
</html>`;
}

function esc(s: string): string {
  return s.replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;');
}

function bold(s: string): string {
  return s.replace(/\*\*([^*]+)\*\*/g, '<strong>$1</strong>');
}

/** Open a print preview window */
export function openPrintPreview(title: string, content: string) {
  const html = generatePrintHTML(title, content);
  const w = window.open('', '_blank');
  if (!w) return;
  w.document.write(html);
  w.document.close();
  setTimeout(() => w.print(), 300);
}
